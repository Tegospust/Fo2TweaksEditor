     private ArrayList<String> loadLines(String aFileName) {
        ArrayList<String> res = new ArrayList<String>();
        try {
            BufferedReader textFile = new BufferedReader(
                          new InputStreamReader(new FileInputStream(aFileName)));
            String line;
            String section = "";
            int linenum = 0;
            while((line = textFile.readLine()) != null) {
                linenum++;
                if (line.startsWith("[")) {
                    section = line;
                }
                else {
                    System.out.printf("%d - %d\n", linenum, line.length());
                    if (line.length() == 0) {
                        res.add(String.format("%d - %s",linenum, line));
                        continue;
                    }
                    if (line.startsWith(";")) {
                        res.add(line);
                        continue;
                    }
                    String keyval = line;
                    String specLine = String.format("%s - %d %s", section, linenum, keyval);
                    res.add(specLine);
                }
            }
        }
        catch (Exception  e) {
            res.clear();
        }
         
         return res;
     }
     
